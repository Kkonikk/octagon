function rect(w,h,L)
%a - side of geks, w - width, h - height, L - length
x0=['OFF'];
x1=[8 4 0;...
w/2 0 0;...
w/2 h 0;...
-w/2 h 0;...
-w/2 0 0;...
w/2 0 L;...
w/2 h L;...
-w/2 h L;...
-w/2 0 L];
x2=[4 1 5 4 0;...
4 2 1 5 6;...
4 2 3 7 6;...
4 3 0 4 7];
dlmwrite('rectangle.off',x0,'')
dlmwrite('rectangle.off',x1,'-append','delimiter',' ')
dlmwrite('rectangle.off',x2,'-append','delimiter',' ')
end
